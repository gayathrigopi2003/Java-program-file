public class Tables{
public static void main(String[]args){
	
for (int n=1; n<=10; n++){
System.out.println("3*"+n+"="+3*n);
}

int n = 1;
while(n<=10){
	System.out.println("4* "+n+"="+4*n);
	n++;
}

int j = 1;
do{
 System.out.println("5* "+j+"="+5*j);
 j++;
}while(j<=10);


}
}